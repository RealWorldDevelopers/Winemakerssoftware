function CelsiusToFahrenheit(celsius) {
   try {
      var fahrenheit = 9.0 / 5.0 * celsius + 32;
      return fahrenheit;
   } catch (err) {
      console.error(err);
   }
}

function FahrenheitToCelsius(fahrenheit) {
   try {
      var celsius = (fahrenheit - 32) * 5.0 / 9.0;
      return celsius;
   } catch (err) {
      console.error(err);
   }
}


